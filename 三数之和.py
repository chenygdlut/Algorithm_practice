import sys
while True:
    line=sys.stdin.readline().strip()
    if line=='':
        break

    l,N=line.split(',')[0],int(line.split(',')[1])
    l=l.split()
    l=[int(s) for s in l]
    l.sort()
    flag=0
    
    for i in range(len(l)-1):
        target=N-l[i]
        k=len(l)-1
        j=i+1
        while j<k:
            while j<k and l[j]+l[k]>target:
                k-=1
            if j<k and l[j]+l[k]==target:
                flag=1
                break
            j+=1
        if flag==1:
            break
    if flag==1:
        print('True')
    else:
        print('False')
        
        